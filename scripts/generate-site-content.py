def collect_rules():
    """Instantiate all rules and merge with config defaults."""
    defaults = LinterConfig.default()
    rules = {}
    for rule_class in BUILTIN_RULES:
        rule = rule_class()
        rule_config = defaults.rules.get(rule.rule_id, {})
        enabled = rule_config.get("enabled", True)

        if enabled == "auto":
            severity_str = f"{rule.default_severity().value} (auto)"
        elif enabled is False:
            severity_str = f"{rule.default_severity().value} (disabled)"
        else:
            severity_str = rule.default_severity().value

        fix_types = []
        if rule.supports_autofix:
            fix_types.append("auto")
        if rule.llm_fix_prompt is not None:
            fix_types.append("llm")

        repo_types_str = ""
        if rule.repo_types:
            repo_types_str = ", ".join(sorted(t.value for t in rule.repo_types))

        rules[rule.rule_id] = {
            "rule_id": rule.rule_id,
            "description": rule.description,
            "severity": severity_str,
            "fix": ", ".join(fix_types) if fix_types else "-",
            "config_schema": rule.config_schema,
            "since": rule.since,
            "repo_types": repo_types_str,
            "llm_fix_prompt": rule.llm_fix_prompt,
            "enabled": enabled,
        }
    return rules


def parse_research():
    """Parse content-rules-research.md into per-rule sections."""
    research_path = ROOT / "docs" / "designs" / "content-rules-research.md"
    if not research_path.exists():
        return {}

    text = research_path.read_text()
    sections = {}
    current_id = None
    current_lines = []

    for line in text.split("\n"):
        m = re.match(r"^## (content-[\w-]+)", line)
        if m:
            if current_id:
                sections[current_id] = "\n".join(current_lines).strip()
            current_id = m.group(1)
            current_lines = []
        elif current_id is not None:
            if line.strip() == "---":
                continue
            current_lines.append(line)

    if current_id:
        sections[current_id] = "\n".join(current_lines).strip()

    # Also grab the cross-cutting and budget sections
    for heading in ["Instruction Budget vs. Context Budget", "Key Papers (Cross-Cutting)"]:
        m = re.search(rf"^## {re.escape(heading)}$", text, re.MULTILINE)
        if m:
            start = m.end()
            next_h2 = re.search(r"^## ", text[start:], re.MULTILINE)
            end = start + next_h2.start() if next_h2 else len(text)
            key = heading.lower().replace(" ", "-").replace("(", "").replace(")", "")
            sections[key] = text[start:end].strip().replace("---", "").strip()

    return sections


def _extract_args(subparser):
    """Extract argument info from an argparse subparser."""
    import argparse as _argparse

    args = []
    for act in subparser._actions:
        if isinstance(act, _argparse._HelpAction):
            continue
        flags = act.option_strings
        if not flags:
            continue
        if act.help == _argparse.SUPPRESS:
            continue

        flag_str = ", ".join(f"`{f}`" for f in flags)
        help_text = act.help or ""
        default = act.default
        if default in (None, False, [], _argparse.SUPPRESS):
            default_str = ""
        elif isinstance(default, Path):
            default_str = ""
        else:
            default_str = str(default)

        choices_str = ""
        if act.choices:
            choices_str = ", ".join(str(c) for c in act.choices)

        args.append({
            "flags": flag_str,
            "help": help_text,
            "default": default_str,
            "choices": choices_str,
        })
    return args


def _extract_subcommands(parser, prefix=""):
    """Extract subcommand info from a parser, recursively for nested subparsers."""
    import argparse as _argparse

    commands = []
    for action in parser._subparsers._actions:
        if not isinstance(action, _argparse._SubParsersAction):
            continue

        help_map = {ca.dest: ca.help for ca in action._choices_actions}

        for name, subparser in action.choices.items():
            full_name = f"{prefix} {name}".strip() if prefix else name
            commands.append({
                "name": full_name,
                "help": help_map.get(name, ""),
                "args": _extract_args(subparser),
            })

            if subparser._subparsers:
                commands.extend(_extract_subcommands(subparser, full_name))

    return commands


def parse_cli():
    """Extract CLI subcommands and arguments by introspecting the real parsers."""
    from skillsaw.__main__ import _build_parser
    from skillsaw.marketplace.cli import _build_add_parser

    parser = _build_parser()
    commands = _extract_subcommands(parser)

    for cmd in commands:
        if cmd["name"] == "add":
            add_parser = _build_add_parser()
            add_subs = _extract_subcommands(add_parser, prefix="add")
            idx = commands.index(cmd)
            commands[idx:idx + 1] = [cmd] + add_subs
            break

    return commands


