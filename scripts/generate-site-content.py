def _rule_table(rule_ids, rules_data, content_link_prefix="content/"):
    """Generate a markdown table for a list of rule IDs."""
    lines = [
        "| Rule ID | Description | Default Severity | Autofix |",
        "|---------|-------------|------------------|---------|",
    ]
    for rule_id in rule_ids:
        r = rules_data[rule_id]
        if rule_id in CONTENT_RULE_IDS:
            short = rule_id.replace("content-", "")
            link = f"[`{rule_id}`]({content_link_prefix}{short}.md)"
        else:
            link = f"`{rule_id}`"
        lines.append(f"| {link} | {r['description']} | {r['severity']} | {r['fix']} |")
    return "\n".join(lines)


def _params_table(rule_id, schema):
    """Generate a config parameters table."""
    lines = [
        f"\n**`{rule_id}` parameters:**\n",
        "| Parameter | Description | Default |",
        "|-----------|-------------|---------|",
    ]
    for param_name, param_info in schema.items():
        desc = param_info["description"]
        default = f"`{json.dumps(param_info['default'])}`"
        lines.append(f"| `{param_name}` | {desc} | {default} |")
    return "\n".join(lines)


def generate_rules_index(rules_data):
    """Generate docs/rules/index.md — overview of all rules."""
    lines = [GENERATED_HEADER, "# Rules Reference\n"]
    lines.append(f"skillsaw includes **{len(rules_data)}** builtin rules ")
    lines.append("organized into the following categories:\n")

    for group_name, slug, rule_ids, description in RULE_GROUPS:
        count = len(rule_ids)
        plural = "rule" if count == 1 else "rules"
        if slug == "content-intelligence":
            lines.append(f"- [{group_name}](content/index.md) ({count} {plural})")
        elif slug in CLAUDE_CODE_SLUGS:
            lines.append(f"- [{group_name}](claude-code/{slug}.md) ({count} {plural})")
        else:
            lines.append(f"- [{group_name}]({slug}.md) ({count} {plural})")

    lines.append("\n## All Rules\n")
    lines.append("| Rule ID | Description | Default Severity | Autofix | Category |")
    lines.append("|---------|-------------|------------------|---------|----------|")

    for group_name, slug, rule_ids, _ in RULE_GROUPS:
        for rule_id in rule_ids:
            r = rules_data[rule_id]
            if rule_id in CONTENT_RULE_IDS:
                link = f"[`{rule_id}`](content/{rule_id.replace('content-', '')}.md)"
            else:
                link = f"`{rule_id}`"
            lines.append(
                f"| {link} | {r['description']} | {r['severity']} | {r['fix']} | {group_name} |"
            )

    return "\n".join(lines) + "\n"


def generate_group_page(group_name, slug, rule_ids, description, rules_data):
    """Generate a per-group rule page."""
    is_content = slug == "content-intelligence"
    lines = [GENERATED_HEADER, f"# {group_name}\n"]

    if description:
        if is_content:
            description = description.replace("../research.md", "../../research.md")
        lines.append(f"{description}\n")

    content_prefix = "" if is_content else "content/"
    lines.append(_rule_table(rule_ids, rules_data, content_link_prefix=content_prefix))
    lines.append("")

    for rule_id in rule_ids:
        r = rules_data[rule_id]
        if r["config_schema"]:
            lines.append(_params_table(rule_id, r["config_schema"]))
            lines.append("")

    return "\n".join(lines) + "\n"


def generate_content_rule_page(rule_id, rules_data, research):
    """Generate an individual content rule page."""
    r = rules_data[rule_id]
    short_name = rule_id.replace("content-", "")
    title = _format_title(short_name)

    lines = [GENERATED_HEADER, f"# {title}\n"]
    lines.append(f"**Rule ID:** `{rule_id}`\n")
    lines.append(f"{r['description']}\n")

    # Metadata badges
    lines.append("| | |")
    lines.append("|---|---|")
    lines.append(f"| **Severity** | {r['severity']} |")
    lines.append(f"| **Autofix** | {r['fix']} |")
    lines.append(f"| **Since** | v{r['since']} |")
    if r["repo_types"]:
        lines.append(f"| **Repo Types** | {r['repo_types']} |")
    lines.append("")

    if r["config_schema"]:
        lines.append("## Configuration\n")
        lines.append("| Parameter | Description | Default |")
        lines.append("|-----------|-------------|---------|")
        for param_name, param_info in r["config_schema"].items():
            desc = param_info["description"]
            default = f"`{json.dumps(param_info['default'])}`"
            lines.append(f"| `{param_name}` | {desc} | {default} |")
        lines.append("")

    research_text = research.get(rule_id, "")
    if research_text:
        lines.append("## Research Basis\n")
        # Rewrite intra-doc anchors to point to the research page
        research_text = research_text.replace(
            "(#instruction-budget-vs-context-budget)",
            "(../../research.md#instruction-budget-vs-context-budget)",
        )
        lines.append(research_text)
        lines.append("")

    return "\n".join(lines) + "\n"


def generate_claude_code_index(rules_data):
    """Generate docs/rules/claude-code/index.md — combined Claude Code rules page."""
    lines = [GENERATED_HEADER, "# Claude Code\n"]
    lines.append(
        "Rules that validate [Claude Code](https://docs.claude.com/en/docs/claude-code) "
        "plugins, commands, marketplaces, agents, hooks, MCP servers, and rules.\n"
    )

    for group_name, slug, rule_ids, description in RULE_GROUPS:
        if slug not in CLAUDE_CODE_SLUGS:
            continue
        lines.append(f"## {group_name}\n")
        if description:
            lines.append(f"{description}\n")
        lines.append(_rule_table(rule_ids, rules_data))
        lines.append("")

        for rule_id in rule_ids:
            r = rules_data[rule_id]
            if r["config_schema"]:
                lines.append(_params_table(rule_id, r["config_schema"]))
                lines.append("")

    return "\n".join(lines) + "\n"


def generate_cli_reference(commands):
    """Generate docs/cli.md from parsed argparse data."""
    lines = [GENERATED_HEADER, "# CLI Reference\n"]
    lines.append("```\nskillsaw [command] [options]\n```\n")

    for cmd in commands:
        depth = cmd["name"].count(" ")
        heading = "#" * (depth + 2)
        lines.append(f"{heading} `skillsaw {cmd['name']}`\n")
        if cmd["help"]:
            lines.append(f"{cmd['help']}\n")

        if cmd["args"]:
            lines.append("| Flag | Description | Default |")
            lines.append("|------|-------------|---------|")
            for arg in cmd["args"]:
                default = f"`{arg['default']}`" if arg["default"] else ""
                desc = arg["help"]
                if arg.get("choices"):
                    desc += f" (choices: {arg['choices']})"
                lines.append(f"| {arg['flags']} | {desc} | {default} |")
            lines.append("")

    return "\n".join(lines) + "\n"


def generate_research_page(research):
    """Generate docs/research.md from the research citations."""
    lines = [GENERATED_HEADER]
    lines.append("# Research & Justification\n")
    lines.append(
        "skillsaw's content intelligence rules analyze the *quality* of AI context "
        "building blocks. Each rule is grounded in published research on LLM behavior, "
        "prompt engineering best practices, or established software engineering principles.\n"
    )

    for rule_id in CONTENT_RULE_IDS:
        if rule_id not in research:
            continue
        short = rule_id.replace("content-", "")
        title = _format_title(short)
        lines.append(f"## [{title}](rules/content/{short}.md)\n")
        lines.append(f"**Rule:** `{rule_id}`\n")
        lines.append(research[rule_id])
        lines.append("\n---\n")

    # Budget comparison section
    budget_key = "instruction-budget-vs.-context-budget"
    if budget_key in research:
        lines.append("## Instruction Budget vs. Context Budget\n")
        lines.append(research[budget_key])
        lines.append("\n---\n")

    # Cross-cutting papers
    papers_key = "key-papers-cross-cutting"
    if papers_key in research:
        lines.append("## Key Papers (Cross-Cutting)\n")
        lines.append(research[papers_key])
        lines.append("")

    return "\n".join(lines) + "\n"


def inject_stats(index_path, rules_data):
    """Replace stat markers in docs/index.md."""
    text = index_path.read_text()
    text = text.replace("<!-- RULE_COUNT -->", str(len(rules_data)))
    index_path.write_text(text)


